using UnityEngine;
using UnityEngine.UI;
using System.Collections;

public class HighlightArrow : MonoBehaviour {
    public Button buttonToHighlight;
    public Sprite highlightObj;
    public Sprite unhighlightObj;
	public void highlight()
    {
        buttonToHighlight.image.sprite = highlightObj;   
    }
    public void unhighlight()
    {
        buttonToHighlight.image.sprite = unhighlightObj;
    }
}
